    <h3>Reservations and Priority Rules</h3>
    <ul>
        <li>Reservation inquiries are accepted no earlier than one (1) week and no later than two (2) hours before the scheduled use. Requests are accepted on a <strong>FIRST COME, FIRST SERVED</strong> basis and apply to blocks without an assigned room unless instructed otherwise by the instructor.</li>
        <li>Room re-assignment or transfer between rooms is prohibited unless authorized by the instructor, S.A. on duty, or Lab Head.</li>
        <li>Allocation priority for laboratory rooms (with ACU):
            <ol>
                <li>CMPE courses</li>
                <li>Sections handled by CPE faculty</li>
                <li>Sections already assigned to that lab room.</li>
            </ol>
        </li>
        <li>If a class with an instructor needs a room currently used by a class without an instructor, the room is reallocated to the class with an instructor; the non-instructor class must vacate and follow the photo & return procedures.</li>
    </ul>
</div>

